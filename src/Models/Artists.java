package Models;

/**
 *
 * @author victor
 */
public class Artists {
    private String name;
    private String bio;
    
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBio() {
        return bio;
    }

    public void setBio(String bio) {
        this.bio = bio;
    }
    
    public Artists(String name, String bio){
        setName(name);
        setBio(bio);
    }
}
